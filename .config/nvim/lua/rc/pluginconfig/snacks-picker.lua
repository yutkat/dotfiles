end, "Grep selection")
map("n", "<Leader>/", function()
	Snacks.picker.grep_word()
end, "Grep word under cursor")
map("n", "[_FuzzyFinder]/", function()
	Snacks.picker.grep_word()
end, "Grep word under cursor")
map("n", "[_FuzzyFinder]*", function()
	Snacks.picker.grep_word()
